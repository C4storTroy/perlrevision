use strict;
use warnings;

main(@ARGV);

sub main
{
    #my $x = 3+7*10 || message("foo");
    my $x = 0 || message("foo");
    message($x);
}

sub message
{
    my $m = shift or return;
    print("$m\n");
}

sub error
{
    my $e = shift || 'unkown error';
    print("$0: $e\n");
    exit 0;
}
