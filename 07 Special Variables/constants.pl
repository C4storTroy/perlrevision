use strict;
use warnings;

main(@ARGV);

sub main
{
    message("the filename is " . __FILE__);
    message("the line is " . __LINE__);
    message("the package is " . __PACKAGE__);
}

sub message
{
    my $m = shift or return;
    print("$m\n");
}

sub error
{
    my $e = shift || 'unkown error';
    print("$0: $e\n");
    exit 0;
}
